const TILE_W = PIXEL * GRID[0].length  // 64

export default function UkrainianBorder({id = 'ua-border', header = false}: {id?: string; header?: boolean}) {
  const grid = header ? GRID_HEADER : GRID
  const TILE_H = PIXEL * grid.length
  return (
    <div style={{height: TILE_H, flexShrink: 0, display: 'block'}}>
      <svg
        width="100%"
        height={TILE_H}
        xmlns="http://www.w3.org/2000/svg"
        aria-hidden="true"
        style={{display: 'block'}}
      >
        <defs>
          <pattern
            id={id}
            x="0"
            y="0"
            width={TILE_W}
            height={TILE_H}
            patternUnits="userSpaceOnUse"
          >
            <rect width={TILE_W} height={TILE_H} fill="white" />
            {grid.flatMap((row, r) =>
              row.map((cell, c) =>
                cell ? (
                  <rect
                    key={`${r}-${c}`}
                    x={c * PIXEL}
                    y={r * PIXEL}
                    width={PIXEL}
                    height={PIXEL}
                    fill={COLORS[cell]}
                  />
                ) : null
              )
            )}
          </pattern>
        </defs>
        <rect width="100%" height={TILE_H} fill={`url(#${id})`} />
      </svg>
    </div>
  )
}
